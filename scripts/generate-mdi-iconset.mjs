#!/usr/bin/env node
/**
 * Generates src/plugins/mdiPaths.js — a map of the mdi-* icon class names used
 * in the app to their @mdi/js SVG path exports. Used by the custom mdi-svg
 * Vuetify iconset (src/plugins/mdiIconset.js) so we ship only the icons we use
 * as inline SVG paths instead of the full Material Design Icons webfont.
 *
 * Run after adding/removing icons:  node scripts/generate-mdi-iconset.mjs
 * Fails (exit 1) if any used icon has no matching @mdi/js export and no override.
 */
import { readdirSync, readFileSync, writeFileSync, statSync } from 'node:fs'
import { join, relative, sep } from 'node:path'
import { fileURLToPath } from 'node:url'
import * as mdi from '@mdi/js'

const ROOT = join(fileURLToPath(new URL('.', import.meta.url)), '..')
const SRC = join(ROOT, 'src')
const OUT = join(SRC, 'plugins', 'mdiPaths.js')
const SCAN_EXT = /\.(vue|js|ts)$/
// Files to skip (the generator output and the iconset wrapper reference mdi-*).
const SKIP = new Set(['src/plugins/mdiPaths.js', 'src/plugins/mdiIconset.js'])

// Icons whose mdi-* class is not a valid @mdi/js export (renamed/removed
// upstream). These render blank in the font build too, so mapping them here
// also fixes a latent bug. Keyed by in-repo class name -> @mdi/js export name.
const OVERRIDES = {
	'mdi-building': 'mdiOfficeBuilding', // mdi-building absent from font + @mdi/js
}

// Normalize to forward slashes so the SKIP check works on Windows too.
const relPosix = (full) => relative(ROOT, full).split(sep).join('/')

const walk = (dir, acc = []) => {
	for (const entry of readdirSync(dir)) {
		const full = join(dir, entry)
		if (statSync(full).isDirectory()) walk(full, acc)
		else if (SCAN_EXT.test(entry) && !SKIP.has(relPosix(full))) acc.push(full)
	}
	return acc
}

const used = new Set()
for (const file of walk(SRC)) {
	const text = readFileSync(file, 'utf8')
	for (const m of text.matchAll(/mdi-[a-z0-9-]+/g)) {
		if (m[0] !== 'mdi-svg') used.add(m[0]) // 'mdi-svg' is a module path, not an icon
	}
}

const toExport = (cls) =>
	OVERRIDES[cls] ??
	'mdi' +
		cls
			.replace(/^mdi-/, '')
			.split('-')
			.map((p) => p.charAt(0).toUpperCase() + p.slice(1))
			.join('')

const names = [...used].sort()
const entries = []
const missing = []
for (const cls of names) {
	const exp = toExport(cls)
	if (exp in mdi) entries.push({ cls, exp })
	else missing.push(`${cls} -> ${exp}`)
}

if (missing.length) {
	console.error(`ERROR: ${missing.length} icon(s) have no @mdi/js export and no override:`)
	for (const m of missing) console.error(`  ${m}`)
	console.error('Add an entry to OVERRIDES in this script, then re-run.')
	process.exit(1)
}

const importNames = [...new Set(entries.map((e) => e.exp))].sort()
const out = `// AUTO-GENERATED by scripts/generate-mdi-iconset.mjs — do not edit by hand.
// Maps the ${entries.length} mdi-* icon names used in the app to @mdi/js SVG paths.
// Regenerate after changing icon usage:  node scripts/generate-mdi-iconset.mjs
import {
${importNames.map((n) => `\t${n},`).join('\n')}
} from '@mdi/js'

export const MDI_NAME_TO_PATH = {
${entries.map((e) => `\t'${e.cls}': ${e.exp},`).join('\n')}
}
`

writeFileSync(OUT, out)
console.log(`Wrote ${relative(ROOT, OUT)} — ${entries.length} icons, ${importNames.length} unique paths.`)
